# Starting Turtle
import turtle
# coloring the shape
turtle.fillcolor('blue')
turtle.begin_fill()
# Drawing the shape
turtle.left(45)
turtle.forward(150)
turtle.right(90)
turtle.forward(150)
turtle.right(90)
turtle.forward(150)
turtle.right(90)
turtle.forward(300)
turtle.left(90)
turtle.forward(150)
turtle.left(90)
turtle.forward(150)
turtle.left(90)
turtle.forward(150)
turtle.end_fill()
# keeping the turtle open when done witht he program.
turtle.done()

